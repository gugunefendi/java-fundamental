package tt_oop;

public class Main {

    public static void main(String[] args) {
        /*
         * OOP
         * apa itu OOP?
         * OOP adalah singkatan dari Object Oriented Programming
         * Sebuah paradigma dalam pemrograman yang berorientasi pada object
         * 
         * 
         * Selain OOP ada paradigma lain yang sering digunakan, tergantung kebutuhan si Programmer, berikut diantaranya:
         * 1. Procedural Programming (Pemrograman Prosedural)
         *      Paradigma ini berfokus pada pada urutan atau prosedur (fungsi).
         *      * Biasanya kamu nulis kode dari atas ke bawah, fungsi demi fungsi
         *      * Contoh bahasa: C, Pascal, bisa juga di pakai di PHP atau Javascript
         *      
         *      Contoh dalam Javascript:
         *      function tambah(a, b){
         *          return a + b;
         *      }
         * 
         *      let hasil = tambah(5, 3);
         * 
         * 2. Fungsional Programming (FP)
         *      Paradigma ini berfokus pada fungsi murni (pure function), tanpa mengubah data secara langsung (immutable)
         *      dan tanpa efek samping (Side Effect).
         *      * Cocok buat program yang kompleks, data driven dan butuh stabilitas tinggi
         *      * Contoh bahasa: Haskell, Elixir, tapi Javascript juga mendukung FP
         * 
         *      Contoh dalam Javascript:
         *      const tambah = (a, b) => a + b;
         *      const hasil = tambah(7, 3);
         * 
         * 3. Declarative Programming
         *      Paradigma ini berfokus pada apa yang ingin dicapai, bukan bagaimana caranya.
         *      Contoh: SQL (kamu cukup bilang SELECT * FROM users, tanpa memikirkan cara loop datanya)
         *      Di Frontend: React pakai cara deklaratif juga
         * 
         * 4. Event-Driven Programming
         *      Paradigma yang befokus pada event/event handler. Kode akan merespon kejadian (event) seperti klik tombol, input user dll
         *      * Banyak dipakai di aplikasi web, UI interaktif
         *      * Contoh: JavaScript (browser), Node.js, Electron
         *      button.eventListener("click", function() {
         *          console.log("you are clicked me");
         *      })
         * 
         * 5. Logic Programming
         *      Paradigma ini berfokus pada logika dan aturan
         *      Contoh: Prolog
         *      Biasanya digunakan untuk sistem pakar, kecerdasan buatan dan pemrosesan bahasa alami
         * 
         * Ringkasan:
         * ---------------------------------------------------------------------
         * Paradigma        Fokusnya                            Contoh Bahasa
         * ---------------------------------------------------------------------
         * Procedural       Urutan instruksi & fungsi           C, PHP, Python
         * OOP              Object                              Java, C++, PHP
         * Functional       Fungsi murni & tanpa efek samping   Haskell, JS
         * Declarative      Hasil akhir, bukan proses           SQL, React.js
         * Event-Driven     Event dan Respons terhadap event    JS (browser)
         * Logic            Aturan dan inferensi logis          Prolog
         * ---------------------------------------------------------------------         *      
         */
    }
    
}
