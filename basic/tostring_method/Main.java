package basic.tostring_method;

public class Main {

    public static void main(String[] args) {
        // toString() = special method that all object inherit,
        //              that returns a string that "textually represents" an object.
        //              can be used both implicity and explicity

        Car car = new Car();

        System.out.println(car.toString()); // print memory address from Car Object

    }
    
}
