package hh_boolean;

public class Main {
    
    public static void main(String[] args) {
        /*
        Boolean
        Boolean adalah tipe data yang mengembalikkan true atau false

        Expresi Boolean
        Expresi boolean mengembalikkan nilai true atau false
        contoh:
        if(10 > 5) { // ini akan mengmbalikkan nilai true
            // code here
        }
        */

        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println(isJavaFun); // true
        System.out.println(isFishTasty); // false

        // boolean expression
        if(10 > 5) { // true
            System.out.println("10 > 5");
        }

    }

}
