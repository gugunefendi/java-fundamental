package mm_forloop;

public class Main {

    public static void main(String[] args) {
        /*
         * for loop sintaks
         * for (statement 1;  statement 2; statement 3) {
         *  // block kode untuk di eksekusi
         * }
         * 
         * statement 1: di eksekusi (satu kali) sebelum eksekusi kode blok
         * statement 2: mendefinisikan kondisi untuk mengeksekusi kode blok
         * statement 3: di eksekusi (setiap kali) setelah kode blok di eksekusi
         */

         for (int i = 0; i < 10; i++) {
            System.out.println(i);
         }


    }
    
}
